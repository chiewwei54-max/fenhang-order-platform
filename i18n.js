let currentLang = loadLang();
let currentTheme = loadTheme();

function loadTheme() {
  const saved = localStorage.getItem(THEME_STORAGE_KEY);
  return SUPPORTED_THEMES.includes(saved) ? saved : "blue";
}

function saveTheme(theme) {
  currentTheme = theme;
  localStorage.setItem(THEME_STORAGE_KEY, theme);
  document.documentElement.setAttribute("data-theme", theme);
}

function loadLang() {
  const saved = localStorage.getItem(LANG_STORAGE_KEY);
  return SUPPORTED_LANGS.includes(saved) ? saved : "zh";
}

function saveLang(lang) {
  currentLang = lang;
  localStorage.setItem(LANG_STORAGE_KEY, lang);
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang === "ms" ? "ms" : "en";
}

function getNested(obj, path) {
  return path.split(".").reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), obj);
}

function t(key, vars = {}) {
  let str = getNested(TRANSLATIONS[currentLang], key) ?? getNested(TRANSLATIONS.zh, key) ?? key;
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, "g"), v);
  });
  return str;
}

function getStatusLabel(status) {
  return t(`status.${status}`) || status;
}

function applyStaticI18n(root = document) {
  root.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  root.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  const titleKey = root.querySelector("[data-i18n-title-page]");
  if (titleKey) document.title = t(titleKey.dataset.i18nTitlePage);

  root.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });
  root.querySelectorAll(".theme-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.theme === currentTheme);
  });
  updatePrefSummary(root);
}

function updatePrefSummary(root = document) {
  const themeLabel = t(`theme.${currentTheme}`);
  root.querySelectorAll(".pref-toggle-summary").forEach((el) => {
    el.textContent = themeLabel;
  });
}

function closePrefPanels() {
  document.querySelectorAll(".pref-panel.is-open").forEach((panel) => {
    panel.classList.remove("is-open");
    const dropdown = panel.querySelector(".pref-dropdown");
    const toggle = panel.querySelector(".pref-toggle");
    if (dropdown) dropdown.hidden = true;
    if (toggle) toggle.setAttribute("aria-expanded", "false");
  });
}

function initPrefPanel() {
  document.querySelectorAll(".pref-panel").forEach((panel) => {
    const toggle = panel.querySelector(".pref-toggle");
    const dropdown = panel.querySelector(".pref-dropdown");
    if (!toggle || !dropdown) return;

    toggle.addEventListener("click", (e) => {
      e.stopPropagation();
      const willOpen = !panel.classList.contains("is-open");
      closePrefPanels();
      if (willOpen) {
        panel.classList.add("is-open");
        dropdown.hidden = false;
        toggle.setAttribute("aria-expanded", "true");
      }
    });

    dropdown.addEventListener("click", (e) => e.stopPropagation());
  });

  document.addEventListener("click", () => closePrefPanels());
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") closePrefPanels();
  });
}

function initThemeSwitcher() {
  document.querySelectorAll(".theme-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      const theme = btn.dataset.theme;
      if (!SUPPORTED_THEMES.includes(theme) || theme === currentTheme) return;
      saveTheme(theme);
      applyStaticI18n();
      closePrefPanels();
    });
  });
}

function initLangSwitcher(onChange) {
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => {
      const lang = btn.dataset.lang;
      if (!SUPPORTED_LANGS.includes(lang) || lang === currentLang) return;
      saveLang(lang);
      applyStaticI18n();
      closePrefPanels();
      if (typeof onChange === "function") onChange();
    });
  });
}

saveLang(currentLang);
saveTheme(currentTheme);
